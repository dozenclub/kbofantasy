function createRosterLineDefault(position, stringArray) {
    var innerHTMLString = '<tr><th scope="row">' + position + '</th><td>' + stringArray[0] + '</td><td>' + stringArray[1] + '</td><td>';
    if(stringArray[2] >= 0) {
        innerHTMLString = innerHTMLString + '<span style="color:red;">&#9650;' + stringArray[2] + '</span></td><td>';
    } else {
        innerHTMLString = innerHTMLString + '<span style="color:blue;">&#9660;' + (-1 * Number(stringArray[2])) + '</span></td><td>';
    }
    if(stringArray[3] >= 0) {
        innerHTMLString = innerHTMLString + '<span style="color:red;">&#9650;' + stringArray[3] + '</span></td><td>';
    } else {
        innerHTMLString = innerHTMLString + '<span style="color:blue;">&#9660;' + (-1 * Number(stringArray[3])) + '</span></td><td>';
    }
    innerHTMLString = innerHTMLString + (100 * Number(stringArray[4]).toFixed(2)) + '%</td></tr>';
    return innerHTMLString;
}

