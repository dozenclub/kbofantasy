function createRosterLineDefault(position, stringArray) {
    var innerHTMLString = '<tr><th scope="row">' + position + '</th><td>' + stringArray[0] + '</td><td>' + stringArray[1] + '</td><td>';
    if(stringArray[2] >= 0) {
        innerHTMLString = innerHTMLString + '<span style="color:red;">&#9650;' + stringArray[2] + '</span></td><td>';
    } else {
        innerHTMLString = innerHTMLString + '<span style="color:blue;">&#9660;' + (-1 * Number(stringArray[2])) + '</span></td><td>';
    }
    if(stringArray[3] >= 0) {
        innerHTMLString = innerHTMLString + '<span style="color:red;">&#9650;' + stringArray[3] + '</span></td><td>';
    } else {
        innerHTMLString = innerHTMLString + '<span style="color:blue;">&#9660;' + (-1 * Number(stringArray[3])) + '</span></td><td>';
    }
    innerHTMLString = innerHTMLString + (100 * Number(stringArray[4])) + '%</td>';
    return innerHTMLString;
}

function updateRoster(n) {

    // highlight and un-highlight buttons
    if(currentRoster > 0) {
        $('#team'+currentRoster).removeClass("btn-primary").addClass("btn-outline-primary");
    }
    $('#team'+n).removeClass("btn-outline-primary").addClass("btn-primary");
    currentRoster = n;

    // initialize
    var key;
    var innerHTMLStringArray;
    var innerHTMLString;
    rosterHitterDefaultBody.innerHTML   = '';
    rosterHitterAdvancedBody.innerHTML  = '';
    rosterPitcherDefaultBody.innerHTML  = '';
    rosterPitcherAdvancedBody.innerHTML = '';

    // update hitter tables
    for(key in hitterPositionDictionary) {
        // read hitter data
        innerHTMLStringArray = rosterHitterData[n-1][key];
        // update default hitter roster table
        rosterHitterDefaultBody.innerHTML = rosterHitterDefaultBody.innerHTML + createRosterLineDefault(hitterPositionDictionary[key], innerHTMLStringArray);
        // update advanced hitter roster table
        innerHTMLString = '<tr><th scope="row">' + hitterPositionDictionary[key] + '</th><td>' + innerHTMLStringArray[0] + '</td><td>';
        innerHTMLString = innerHTMLString + innerHTMLStringArray.slice(hitterAdvancedIndexRange[0], hitterAdvancedIndexRange[1]+1).join('</td><td>') + '</td>';
        rosterHitterAdvancedBody.innerHTML = rosterHitterAdvancedBody.innerHTML + innerHTMLString;
    }

    // update pitcher tables
    for(key in pitcherPositionDictionary) {
        // read pitcher data
        innerHTMLStringArray = rosterPitcherData[n-1][key];
        // update default pitcher roster table
        rosterPitcherDefaultBody.innerHTML = rosterPitcherDefaultBody.innerHTML + createRosterLineDefault(pitcherPositionDictionary[key], innerHTMLStringArray);
        // update advanced pitcher roster table
        innerHTMLString = '<tr><th scope="row">' + pitcherPositionDictionary[key] + '</th><td>' + innerHTMLStringArray[0] + '</td><td>';
        innerHTMLString = innerHTMLString + innerHTMLStringArray.slice(pitcherAdvancedIndexRange[0], pitcherAdvancedIndexRange[1]+1).join('</td><td>') + '</td>';
        rosterPitcherAdvancedBody.innerHTML = rosterPitcherAdvancedBody.innerHTML + innerHTMLString;
    }
}

window.onload = function() {
    updateRoster(1);
}